
data = ["Phone", "Notebook", "Headphones"]

# Prints whole list
print("Prints whole list")
print(data)
print("\n")

# Prints list by given range
print("Prints list by given range")
print(data[0:2])
print("\n")

# Changes value in list by given value
print("Changes value in list by given value")
data[2] = "Game laptop"
print(data)
print("\n")

# Printing list in reverse order
print("Printing list in reverse order")
print(data[::-1])
print("\n")